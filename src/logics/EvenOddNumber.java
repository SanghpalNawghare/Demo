public class EvenOddNumber {

	public static void main(String[] args) 
	{
       int num=50;
       
       if (num%2==0) 
       {
		 System.out.println("Given Number is Even");
	   } 
       else
       {
         System.out.println("Given Number is Odd"); 
       }
	
	}

}
